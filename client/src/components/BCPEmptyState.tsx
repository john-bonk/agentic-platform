export function BCPEmptyState({ processId }: BCPEmptyStateProps) {
  const bcps = getBCPsByProcessId(processId);
  
  if (bcps.length > 0) {
    return <BCPTableView bcps={bcps} processId={processId} />;
  }
  
  return <EmptyStateView processId={processId} />;
}
